package problems;

public class Decrement {
    public static void main(String[] args) {
        // Create int type variable and call it problem
        int problem = 5;

        // Print solution function
        System.out.println(solution(problem));

    }

    // This function takes an integer and decrements it by one
    public static int solution(int x){
        return x - 1;
    }
}
