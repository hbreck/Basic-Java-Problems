package problems;

public class SquareANumber {
    public static void main(String[] args) {
        // Create variable to be used in solution function
        int x = 5;

        // Print solution function
        System.out.println(solution(x));
    }

    // This function takes an integer and returns the square of that integer
    public static int solution(int x){
        return x * x;
    }
}
